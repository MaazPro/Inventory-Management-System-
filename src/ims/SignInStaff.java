    //checks values in the fields provided
    public boolean checkValues(){
    
        boolean flag = true ;
     if (txt_userName.getText().isEmpty())   
     {
         JOptionPane.showMessageDialog(rootPane, "Please Enter User Name");
         flag = false;
     }
     else if (String.valueOf(Password.getPassword()).isEmpty())
     {
         JOptionPane.showMessageDialog(rootPane, "Please Enter Password");
         flag = false;
     }
     else {
         flag = true;
         
     }
    return (flag);
    }
    
    
