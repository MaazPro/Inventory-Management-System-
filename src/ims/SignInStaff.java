    // checks the provided credential with data stored in database
    public boolean logIn(){
    
    boolean FLAG  = false;    
    String name,sql = "select user_name , password from employees where user_name = ? and password = ?" ;
    Connection con = null;
    PreparedStatement pst = null;
    ResultSet rs = null;
    
    
    
    try{
        con = getCon();
        pst = con.prepareStatement(sql);
        name = txt_userName.getText();
        pst.setString(1,name );
        pst.setString(2, String.valueOf(Password.getPassword()));
        
        rs = pst.executeQuery();
        
        if(rs.next()){
        
            JOptionPane.showMessageDialog(rootPane, "Login Successful");
            FLAG  = true;
        }else {
            JOptionPane.showMessageDialog(rootPane, "Login Unsuccessful ! \nPlease Enter The Correct Password and User Name");
            FLAG  = false;
        }
        
    }catch(Exception ex){
        System.out.println(ex.getMessage()); 
    }
    
    return(FLAG);
    }
    
