    public void createAccount(){
    
    String sql;
                             //                              int                  int
    sql = "insert into employees (user_name,first_name,last_name,age,gender,address,phone_number,password) value(?,?,?,?,?,?,?,?)";
    Connection con = null;
    PreparedStatement pst = null;
    boolean inserted = false ;
    
    try{
        
        con = getConnection();
        pst = con.prepareStatement(sql);
        
        pst.setString(1, txt_userName.getText());
        pst.setString(2, txt_firstName.getText());
        pst.setString(3, txt_lastName.getText());
        
        int AGE = Integer.parseInt(txt_age.getText());
        pst.setInt(4, AGE);
        
        // gender
        pst.setString(5, g); 
        
        
        pst.setString(6, txt_address.getText());
        
        pst.setString(7, txt_phoneNumber.getText());
        
        pst.setString(8, getPassword());
        
        pst.execute();
        JOptionPane.showMessageDialog(rootPane, "Account Created");
        JOptionPane.showMessageDialog(rootPane, "Your User Name is : " + txt_userName.getText());
        con.close();
    }catch(Exception ex){
        ex.printStackTrace();
    }
    
    }
     
    
    
