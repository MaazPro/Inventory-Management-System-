    private void btn_signUpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_signUpActionPerformed
        
        int lengthUN ,lengthP;
        
        lengthUN = txt_userName.getText().toCharArray().length;
        lengthP = Password.getText().toCharArray().length;
        
        if ((flag() == true) &&(CheckUserName() == false))
        {
            if((lengthP < 4))
            {
                JOptionPane.showMessageDialog(rootPane, "Password length has to be more than 3 characters");
            }
            else if (lengthUN < 3)
            {
                JOptionPane.showMessageDialog(rootPane, "User Name length has to be more than 2 characters");
            }
            else
            {
            createAccount();
            clear();
            }
        }
        
    }//GEN-LAST:event_btn_signUpActionPerformed

