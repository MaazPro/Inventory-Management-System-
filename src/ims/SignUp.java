    // Check Input Fields
    public boolean flag (){
    
     boolean FLAG ;       
        
     if(txt_userName.getText().isEmpty())
     {
        JOptionPane.showMessageDialog(rootPane, "Please Enter User Name");
        FLAG = false;    
     }
     else if( txt_firstName.getText().isEmpty())
     {
         JOptionPane.showMessageDialog(rootPane, "Please Enter Name");
         FLAG = false;
     }   
     else if(txt_lastName.getText().isEmpty())
     {
         JOptionPane.showMessageDialog(rootPane, "Please Enter Last Name");
         FLAG = false;
     }     
     else if(txt_age.getText().isEmpty())
     {
         JOptionPane.showMessageDialog(rootPane, "Please Enter Age");
         FLAG = false;
     }
     else if(   ! ( CheckBox_male.isSelected() || CheckBox_female.isSelected() )  )
     {
         JOptionPane.showMessageDialog(rootPane, "Please Select A Gender");
         FLAG = false;
     }
     else if(txt_address.getText().isEmpty())
     {
         JOptionPane.showMessageDialog(rootPane, "Please Enter Address");
         FLAG = false;
     }
     else if(txt_phoneNumber.getText().isEmpty())
     {
         JOptionPane.showMessageDialog(rootPane, "Please Enter Phone Number");
         FLAG = false;
     }
     else if(getPassword().isEmpty())
     {
         JOptionPane.showMessageDialog(rootPane, "Please Enter Password");
         FLAG = false;
     } 
     else if(g == null)
     {
         JOptionPane.showMessageDialog(rootPane, "Please Select a Gender");
         FLAG = false;
     }
     
     else {
     FLAG = true;
    
     }
      return (FLAG);
    }
    
    public boolean CheckUserName(){
        
    boolean check  = false;
    String sql = "select user_name from employees where user_name = ?";
    Connection con = null;
    PreparedStatement pst = null;
    ResultSet rs;
   
   if(txt_userName.getText().isEmpty())
   {
       JOptionPane.showMessageDialog(rootPane, "Please Enter User Name");
   }
   else{
       
    try{
    
        con = getConnection();
        
        pst = con.prepareStatement(sql);
        pst.setString(1,txt_userName.getText() );
        
        rs = pst.executeQuery();
        
        if(rs.next()){
        
        JOptionPane.showMessageDialog(rootPane, "This User Name Already Exist");    
        check = true;
       
        }else{
            check = false; 
        }
        
    }catch (Exception ex){
        ex.printStackTrace();
    }
    
   }
    return (check);
    }
    
